def ask_truth_or_dare(players):
    results = []

    for player in players:
        while True:
            choice = input(f"{player}, обери Правда або Дія: ").strip()

            if choice == "правда":
                results.append("Правда")
                break
            elif choice == "дія":
                results.append("Дія")
                break
            else:
                print("Будь ласка, введіть 'Правда' або 'Дія'")

    return results
