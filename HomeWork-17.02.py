def get_player_names():
    players = []

    for i in range(2):
        name = input(f"Гравець {i + 1}: ")
        players.append(name)

    while True:
        add_player = input("Хочете додати ще одного гравця? (так/ні): ")

        if add_player.lower() == 'так':
            name = input(f"Гравець {len(players) + 1}: ")
            players.append(name)
        else:
            break

    return players

