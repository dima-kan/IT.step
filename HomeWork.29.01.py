# Після чого виведіть статистику скільки людей як саме
# відповіли у форматі:
# 10 – негативно
# 27 – скоріше негативно

answers = ["негативно", "скоріше негативно", "нейтрально", "скоріше позитивно", "позитивно"]
counts = [0, 0, 0, 0, 0]

while True:
    user_input = input("Введіть варіанти відповідей ").strip().lower()

    if user_input == "домашнє завдання":
        break

    if user_input == answers[0]:
        counts[0] += 1
    elif user_input == answers[1]:
        counts[1] += 1
    elif user_input == answers[2]:
        counts[2] += 1
    elif user_input == answers[3]:
        counts[3] += 1
    elif user_input == answers[4]:
        counts[4] += 1
    else:
        print("Помилка! Введіть один з варіантів.")

print("Статистика відповідей:")
print(counts[0], "– негативно")
print(counts[1], "– скоріше негативно")
print(counts[2], "– нейтрально")
print(counts[3], "– скоріше позитивно")
print(counts[4], "– позитивно")